    private void reiniciaJogo() {
        for (Component c: this.painelPrincipal.getComponents()){
            if(c instanceof JToggleButton){
                ((JToggleButton) c).setEnabled(true);
                ((JToggleButton) c).setSelected(false);
                ((JToggleButton) c).setBackground(Color.darkGray);
            }
        }

        jogo = new JogoCampoMinado(10,valorDificuldade);

        numTentativas.setText(String.valueOf(jogo.getNumTentativas()));

    }

    private void mostraBombas(){
        int i = 0;
        for (Component c: this.painelPrincipal.getComponents()){
            if(jogo.getPosicoesBombas().contains(i)){
                if(c instanceof JToggleButton){
                    ((JToggleButton) c).setBackground(Color.red);
                }
            }
            else{
                if(c instanceof JToggleButton){
                    ((JToggleButton) c).setEnabled(false);
                }
            }
            i++;
        }
    }

    private boolean verificaExplosao(JToggleButton botao){
        int i = 0;
        for (Component c: this.painelPrincipal.getComponents()){
            if(jogo.getPosicoesBombas().contains(i)){
                if(c instanceof JToggleButton){
                    if(c.equals(botao)){
                        ((JToggleButton) c).setBackground(Color.red);
                        return true;
                    }
                }
            }
            i++;
        }
        botao.setEnabled(false);
        return false;
    }

