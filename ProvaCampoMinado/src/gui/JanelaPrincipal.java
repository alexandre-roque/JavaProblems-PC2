    private void botaoPressionadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPressionadoActionPerformed
        jogo.realizaJogada();
        numTentativas.setText(String.valueOf(jogo.getNumTentativas()));

        if(verificaExplosao(((JToggleButton)evt.getSource()))){
            this.mostraBombas();
            JOptionPane.showMessageDialog(this, "Voc perdeu!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            int input = JOptionPane.showConfirmDialog(null, "Deseja jogar novamente? ");

            if(input == 0){
                reiniciaJogo();
            }
        }
        else{
            if(confereVitoria()){
                this.mostraBombas();
                JOptionPane.showMessageDialog(this, "Voc ganhou!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                int input = JOptionPane.showConfirmDialog(null, "Deseja jogar novamente? ");

                if(input == 0){
                    reiniciaJogo();
                }

            }
        }


    }//GEN-LAST:event_botaoPressionadoActionPerformed

